        # –ü–µ—Ä–≤–æ–µ —á–∏—Å–ª–æ –∫–∞–∂–¥–æ–≥–æ –º–µ—Å—è—Ü–∞ –≤ 10:00
        self.scheduler.add_job(
            self._send_monthly_stats,
            CronTrigger(day=1, hour=10, minute=0),
            id='monthly_stats'
        )
    
